/** Get the Phaser texture key for a portrait ID. */
export function getPortraitTextureKey(portraitId: string): string {
  return `portrait-${portraitId}`;
}

/** Get the asset path (relative to public/) for a portrait. */
export function getPortraitAssetPath(def: PortraitDefinition): string {
  return `portraits/ceo/${def.filename}`;
}

/** Get a portrait definition by ID. */
export function getPortraitById(
  portraitId: string,
): PortraitDefinition | undefined {
  return CEO_PORTRAITS.find((p) => p.id === portraitId);
}

/** Pick a random portrait, optionally filtered by category. */
export function pickRandomPortrait(
  rng: SeededRNG,
  excludeIds: string[] = [],
  category?: PortraitCategory,
): CharacterPortrait {
  let pool = CEO_PORTRAITS.filter((p) => !excludeIds.includes(p.id));
  if (category) {
    pool = pool.filter((p) => p.category === category);
  }
  if (pool.length === 0) {
    pool = CEO_PORTRAITS;
  }
  const pick = rng.pick(pool);
  return { portraitId: pick.id, category: pick.category };
}

/** Generate a random CEO name. */
export function generateCEOName(rng: SeededRNG): string {
  return `${rng.pick(AI_CEO_FIRST_NAMES)} ${rng.pick(AI_CEO_LAST_NAMES)}`;
}

/** Generate a random empire leader name with title. */
export function generateLeaderName(rng: SeededRNG): string {
  const title = rng.pick(EMPIRE_LEADER_TITLES);
  const first = rng.pick(AI_CEO_FIRST_NAMES);
  const last = rng.pick(AI_CEO_LAST_NAMES);
  return `${title} ${first} ${last}`;
}
